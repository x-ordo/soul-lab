// ============================================================
// Prompt Selection Helper
// ============================================================

export type ConsultationType =
  | 'daily'
  | 'weekly'
  | 'monthly'
  | 'yearly'
  | 'love'
  | 'career'
  | 'health'
  | 'yesno'
  | 'choice'
  | 'chat';

export function getSystemPrompt(type: ConsultationType): string {
  switch (type) {
    case 'daily':
      return WEEKLY_REPORT_PROMPT; // Daily는 provider.ts의 DAILY_FORTUNE 사용
    case 'weekly':
      return WEEKLY_REPORT_PROMPT;
    case 'monthly':
      return MONTHLY_REPORT_PROMPT;
    case 'yearly':
      return YEARLY_REPORT_PROMPT;
    case 'love':
      return LOVE_CONSULTATION_PROMPT;
    case 'career':
      return CAREER_CONSULTATION_PROMPT;
    case 'health':
      return HEALTH_CONSULTATION_PROMPT;
    case 'yesno':
      return YES_NO_PROMPT;
    case 'choice':
      return CHOICE_PROMPT;
    case 'chat':
    default:
      return CASUAL_CHAT_PROMPT;
  }
}
